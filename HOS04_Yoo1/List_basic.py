# Create a list
motorcycles = ['honda', 'yamaha', 'suzuki']
print(motorcycles)

# Access elements
print(motorcycles[0])
print(motorcycles[1])
print(motorcycles[2])

# Update element
motorcycles[0] = 'ducati'
print(motorcycles)

# Append elements
motorcycles.append('harley')
print(motorcycles)

# Insert element at index 1
motorcycles.insert(1, 'kawasaki')
print(motorcycles)
